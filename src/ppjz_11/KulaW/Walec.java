package ppjz_11.KulaW;

public class Walec {
    private int wysokość;
    public int promień;

    public Walec(int wysokość, int promień){
        this.wysokość = wysokość;
        this.promień = promień;
    }
    public int getWysokość() {

        return wysokość;
    }

    public int getPromień() {
        return promień;
    }

}
